package com.rsy.constructor;

public class Boy {
	
	private String name;
	private int age;
	
	public Boy(String name,int age){
		this.name = name;
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String n) {
		this.name = n;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int a) {
		this.age = a;
	}

	
	
}
